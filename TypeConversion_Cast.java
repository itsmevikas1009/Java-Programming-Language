import java.util.*;

public class TypeConversion_Cast {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int a = 10;
        float b = a;
        System.out.println(b);
        int c = 200;
        long d= c;
        System.out.println(d);
        float e = sc.nextInt();
        System.out.println(e);
    }
}
